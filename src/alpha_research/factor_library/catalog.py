def _report_rc_eps_diffusion(window, min_n=3, min_active=2):
    """report_rc analyst EPS-revision breadth (eps_diffusion): net % of analysts RAISING
    their FY1 EPS over `window` trading days, gated on >= min_n revisions AND >= min_active
    live analysts. Built from the approved $report_rc__* event-flow primitives; each field is
    Ref(...,1)-wrapped (on top of the provider's report_date+1 anchor) per the field-registry
    PIT contract. Sparse event feed -> Sum(If(x==x,x,0), W) zero-fills no-event days; ineligible
    rows -> np.nan (drop from the cross-section, NOT 0). Verified bit-identical to the sandbox
    screen (corr 1.0). Coverage is cap-tilted (~27% @60d) -> size-neutralize when used."""
    up = "Ref($report_rc__eps_up, 1)"; dn = "Ref($report_rc__eps_dn, 1)"
    rc = "Ref($report_rc__eps_revision_count, 1)"; act = "Ref($report_rc__n_active_analysts, 1)"
    z = lambda x: f"If(({x}) == ({x}), ({x}), 0)"          # NaN!=NaN -> 0 (zero-fill no-event days)
    up_w, dn_w, n_w = f"Sum({z(up)}, {window})", f"Sum({z(dn)}, {window})", f"Sum({z(rc)}, {window})"
    enough = f"({n_w} >= {min_n})"; active = f"({act} >= {min_active})"
    denom = f"If({enough}, {n_w}, 1)"; raw = f"(({up_w} - {dn_w}) / {denom})"
    return f"If({enough}, If({active}, {raw}, np.nan), np.nan)"


