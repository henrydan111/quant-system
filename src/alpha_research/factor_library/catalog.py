def get_category_map():
    """Return dict mapping each factor name to its category.

    Returns:
        Dict of {factor_name: category_string}.
    """
    catalog = get_factor_catalog(include_new_data=True)
    mapping = {}
    for name in catalog:
        prefix = name.split('_')[0]
        category_map = {
            'mom': 'Momentum',
            'rev': 'Reversal',
            'val': 'Value',
            'qual': 'Quality',
            'grow': 'Growth',
            'size': 'Size',
            'risk': 'Volatility',
            'liq': 'Liquidity',
            'tech': 'Technical',
            'lev': 'Leverage',
            'flow': 'Capital Flow',
            'north': 'Northbound',
            'margin': 'Margin',
            'earn': 'Earnings',
            'comp': 'Composite',
        }
        mapping[name] = category_map.get(prefix, 'Other')
    return mapping
