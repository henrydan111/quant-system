def generate_dictionary():
    data_dir = r"e:\量化系统\data"
    output_path = r"e:\量化系统\data\data_dictionary.md"

    # Define the mapping of files to groups
    file_groups = {
        "1. Reference Data (参考数据)": [
            (r"reference\stock_basic.parquet", "stock_basic (股票基础信息)"),
            (r"reference\trade_cal.parquet", "trade_cal (交易日历)")
        ],
        "2. Market Daily Data (市场日线数据)": [
            (r"market\daily\*\*.parquet", "daily (日线行情 & 每日指标)"),
            (r"market\index\*.parquet", "index (指数行情)")
        ],
        "3. Fundamentals Data (财务基础数据)": [
            (r"fundamentals\income\*.parquet", "income (利润表)"),
            (r"fundamentals\balancesheet\*.parquet", "balancesheet (资产负债表)"),
            (r"fundamentals\indicators\*.parquet", "indicators (财务指标)")
        ],
        "4. Corporate Actions (公司事件)": [
            (r"corporate\dividends\*.parquet", "dividends (分红送股)")
        ],
        "5. Universe & Reference (基准与成分股)": [
            (r"universe\index_weights\*.parquet", "index_weights (指数权重)"),
            (r"universe\industry_sw2021\*.parquet", "industry_sw2021 (申万行业2021)")
        ]
    }

    with open(output_path, "w", encoding="utf-8") as f:
        f.write("# Data Dictionary (数据字典)\n\n")
        f.write("This document provides an **exhaustive** dictionary of all data features available in the local Parquet cache. ")
        f.write("It corresponds to every single column present in the raw files.\n\n")

        for section, items in file_groups.items():
            f.write(f"## {section}\n\n")
            for glob_pattern, title in items:
                search_path = os.path.join(data_dir, glob_pattern)
                files = glob.glob(search_path)
                if not files:
                    continue
                # just pick the first file found
                df = pd.read_parquet(files[0])
                columns = df.columns.tolist()

                f.write(f"### {title}\n")
                f.write(f"Total Columns: {len(columns)}\n\n")
                f.write("| Column | English | Chinese |\n")
                f.write("|--------|---------|---------|\n")

                for col in columns:
                    desc_en, desc_cn = TS_META_DICT.get(col, (col.replace("_", " ").title(), "未知/自定义"))
                    f.write(f"| `{col}` | {desc_en} | {desc_cn} |\n")
                f.write("\n")

    print(f"Generated data dictionary at {output_path}")

if __name__ == "__main__":
    generate_dictionary()
